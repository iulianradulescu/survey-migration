package ro.digidata.esop.input;

import java.util.Scanner;
import org.springframework.boot.CommandLineRunner;
import org.springframework.stereotype.Component;

@Component
public class ShellRunner {

    private String handleInput() {
        //promp the user
        promptUser();

        //read the input
        Scanner sc = new Scanner(System.in);
        String input = sc.nextLine();

        return input;
    }

    private void promptUser() {
        System.out.print("> ");
    }

    public void handleAllInputs( ) {
        try {
            //continue with the standard input; it will exit via a QuitException generated by a QuitCommand
            while (true) {
                try {
                String command = handleInput( );
                System.out.println( command );
                } catch (Exception  ex ) {
                    //invalid command; retry again
                    System.out.println("Introduceti din nou comanda:");
                }
            }
        } catch (Exception  exQE) {
            //gracefully ended it; nothing to do hear
        }
    }
}
